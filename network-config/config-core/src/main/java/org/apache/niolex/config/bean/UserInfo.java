package org.apache.niolex.config.bean;

/**
 * This bean is corresponding to the table of user_info in the database.
 *
 * @author <a href="mailto:xiejiyun@gmail.com">Xie, Jiyun</a>
 * @version 1.0.0
 * @Date: 2012-7-6
 */
public class UserInfo {

	/**
	 * The user name;
	 */
	private String userName;

	/**
	 * The password.
	 */
	private String password;

	/**
	 * The user id.
	 */
	private int userId;

	/**
	 * The user role.
	 */
	private String userRole;

	//---------------------- GETTER & SETTER ---------------------------------

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getUserRole() {
		return userRole;
	}

	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("[userName=").append(userName).append(", password=").append(password)
				.append(", userId=").append(userId).append(", userRole=").append(userRole).append("]");
		return builder.toString();
	}

}
